<p>Once header file is done, let's move on to implementation file, <b>mytcpsocket.cpp</b>:</p>

<pre>
#include "mytcpsocket.h"

MyTcpSocket::MyTcpSocket(QObject *parent) :
    QObject(parent)
{
}

void MyTcpSocket::doConnect()
{
    socket = new <font color="red">QTcpSocket</font>(this);

    socket-><font color="blue">connectToHost</font>("google.com", 80);

    if(socket-><font color="blue">waitForConnected</font>(5000))
    {
        qDebug() << "Connected!";

        // send
        socket-><font color="blue">write</font>("Hello server\r\n\r\n");
        socket-><font color="blue">waitForBytesWritten</font>(1000);
        socket-><font color="blue">waitForReadyRead</font>(3000);
        
        qDebug() << "Reading: " << socket-><font color="blue">bytesAvailable</font>();

        // get the data
        qDebug() << socket-><font color="blue">readAll</font>();
        
        // close the connection
        socket-><font color="blue">close</font>();
    }
    else
    {
        qDebug() << "Not connected!";
    }
}
</pre>
<p>The <b>void QAbstractSocket::connectToHost(const QString & hostName, quint16 port, OpenMode openMode = ReadWrite, NetworkLayerProtocol protocol = AnyIPProtocol)</b> function 
attempts to make a connection to hostName on the given port. The protocol parameter can be used to specify which network protocol to use (eg. IPv4 or IPv6).</p>
<p>
The socket is opened in the given openMode and first enters HostLookupState, then performs a host name lookup of hostName. If the lookup succeeds, hostFound() is emitted and QAbstractSocket enters ConnectingState. It then attempts to connect to the address or addresses returned by the lookup. Finally, if a connection is established, QAbstractSocket enters ConnectedState and emits connected().</p>
<p>
At any point, the socket can emit error() to signal that an error occurred.</p>
<p>The hostName may be an IP address in string form (e.g., "43.195.83.32"), or it may be a host name (e.g., "example.com"). QAbstractSocket will do a lookup only if required. port is in native byte order.</b>
<p>The <b>bool QAbstractSocket::waitForConnected(int msecs = 30000)</b> waits until the socket is connected, up to msecs milliseconds. If the connection has been established, this function returns true; otherwise it returns false. In the case where it returns false, we can call error() to determine the cause of the error.</p>
<p>For buffered devices, the <b>bool QIODevice::waitForBytesWritten(int msecs)</b> function waits until a payload of buffered written data has been written to the device and the bytesWritten() signal has been emitted, or until msecs milliseconds have passed. If msecs is -1, this function will not time out. For unbuffered devices, it returns immediately.</p>
<p>It returns true if a payload of data was written to the device; otherwise returns false (i.e. if the operation timed out, or if an error occurred).</p>
<p>
This function can operate without an event loop. It is useful when writing non-GUI applications and when performing I/O operations in a non-GUI thread.</p>
<p>The <b>bool QAbstractSocket::waitForReadyRead(int msecs = 30000)</b> is 
reimplemented from QIODevice::waitForReadyRead().</p>
<p>
This function blocks until new data is available for reading and the readyRead() signal has been emitted. The function will timeout after msecs milliseconds; the default timeout is 30000 milliseconds.</p>
<p>
The function returns true if the readyRead() signal is emitted and there is new data available for reading; otherwise it returns false (if an error occurred or the operation timed out).</p>
<p>The <b>qint64 QAbstractSocket::bytesAvailable() const</b> is reimplemented from QIODevice::bytesAvailable().</p>
<p>This function returns the number of incoming bytes that are waiting to be read.</p>
<p>The <b>QByteArray QIODevice::readAll()</b> reads all available data from the device, and returns it as a QByteArray.</p>
