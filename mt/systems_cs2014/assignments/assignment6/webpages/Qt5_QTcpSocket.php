<div class="subtitle" id="QTcpSocket">QTcpSocket</div>
<p>In this tutorial, we will learn how to download a file using <a href="http://qt-project.org/doc/qt-5.0/qtnetwork/qtcpsocket.html" target="_blank">QTcpSocket</a>. We're taking blocking approach (synchronous). In other words, we use <b>waitFor...()</b> functions (e.g., <b>QTcpSocket::waitForConnected()</b>) until the operation has completed, instead of connecting to signals.</p>
<p>Note: Qt5 document<br />
<blockquote>
<p>The QTcpSocket class provides a TCP socket.</p>
<p>TCP (Transmission Control Protocol) is a reliable, stream-oriented, connection-oriented transport protocol. It is especially well suited for continuous transmission of data.</p>
<p>QTcpSocket is a convenience subclass of QAbstractSocket that allows you to establish a TCP connection and transfer streams of data. See the QAbstractSocket documentation for details.</p>
</blockquote>
<p>For TCP Socket in general, please visit my C++ Tutorials: <a href="http://www.bogotobogo.com/cplusplus/sockets_server_client.php" target="_blank">Socket - Server and Client</a>.</p>


